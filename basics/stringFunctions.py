string1 = 'hello, My name is Md Sakib Hasan'
print('The string is :',string1)

#slicing
print('sliced part is: ', string1[11:20])

#endwith
print('last charecter of the string is N:',string1.endswith('n'))

#Capitalize
print('After capitalize the string:',string1.capitalize())

